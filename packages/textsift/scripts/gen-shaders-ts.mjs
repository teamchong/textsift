#!/usr/bin/env node
// Regenerate `src/browser/backends/shaders.ts` from the canonical
// `.wgsl` files. After editing a shader source, run this script so
// the browser bundle picks up the new WGSL string.
//
// Native (Zig) reads the .wgsl files via @embedFile at compile time,
// so it doesn't need a regen step — `npm run build:native` is
// enough.

import { readFile, writeFile, readdir } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = join(HERE, "..");
const SHADERS_DIR = join(REPO, "src", "native", "shaders");
const SHADERS_TS = join(REPO, "src", "browser", "backends", "shaders.ts");

const files = (await readdir(SHADERS_DIR)).filter((f) => f.endsWith(".wgsl")).sort();

let out = "// AUTO-GENERATED by scripts/gen-shaders-ts.mjs.\n";
out += "// Source of truth: ../../native/shaders/*.wgsl\n";
out += "// Edit the .wgsl files, then re-run the script.\n\n";
for (const f of files) {
  const body = await readFile(join(SHADERS_DIR, f), "utf8");
  const ident = f.replace(/\.wgsl$/, "").toUpperCase() + "_WGSL";
  out += `export const ${ident}: string = ${JSON.stringify(body)};\n\n`;
}
await writeFile(SHADERS_TS, out, "utf8");
console.log(`wrote ${SHADERS_TS}  (${files.length} shaders)`);
